                <p class="text-justify">Los clientes del programa de lealtad Socio SyD, podrán acceder a diferentes
                    beneficios al alcanzar cada uno de los niveles definidos por los montos de compra mensual en
                    productos SyD. Habrá dos tipos de clientes registrados dentro del programa de lealtad: Clientes
                    individuales y Clientes Corporativos.<br><br>

                    Los clientes individuales tendrán acceso a cada uno de estos beneficios al alcanzar el monto de
                    compra mensual requerido, mientras que los clientes corporativos tendrán la oportunidad de registrar
                    a beneficiarios dentro de su cuenta, a quienes podrá transferirles los beneficios alcanzados por el
                    monto de compra mensual requerido en productos SyD.<br><br>

                    Al alcanzar el mínimo de compra mensual requerido para llegar al primer nivel, el cliente tendrá
                    acceso a un seguro de accidentes personales con cobertura de muerte accidental e invalidez total y
                    permanente por accidentes. Dicho seguro se puede aplicar para el cliente individual o para los
                    beneficiarios que designe el cliente corporativo. Más detalles sobre la cobertura de este beneficio,
                    así como los montos asegurados de cada rubro y números de contacto en caso de ser necesario utilizar
                    el seguro, pueden ser consultados dentro del sitio web del programa de lealtad, en la sección
                    correspondiente al seguro de accidentes.

                </p>

