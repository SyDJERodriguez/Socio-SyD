                    Más información sobre los requisitos para aplicar al crédito y cómo acceder al mismo, podrán ser
                    consultados dentro del sitio web del programa de lealtad, en la sección correspondiente a
                    créditos.<br><br>

                    El beneficio del seguro de accidentes personales, así como el beneficio de asistencias personales,
                    tendrá una vigencia de 1 mes aplicable al siguiente mes de que el cliente haya alcanzado el monto de
                    compra requerido para acceder a dichos beneficios. Por ejemplo, si el cliente alcanza el monto
                    requerido para acceder a ambos beneficios durante el mes de enero, dichos beneficios serán activados
                    durante todo el mes de febrero sin importar el monto de compra que el cliente haga durante ese mes.

