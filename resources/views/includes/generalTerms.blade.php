                    el cliente tendrá acceso a un beneficio de asistencia médica telefónica, ambulancia terrestre por
                    emergencia o asistencia funeraria por accidente. Este beneficio se suma al beneficio del seguro de
                    accidentes personales obtenido al llegar al nivel uno.<br><br>>

                    Más detalles sobre la cobertura de los beneficios de asistencia, los límites de cada evento, el
                    monto de cobertura de la asistencia funeraria y el número al cuál comunicarse en caso de algún
                    evento de cobertura, pueden ser consultados dentro del sitio web del programa de lealtad, en la
                    sección correspondiente al beneficio de asistencias personales. Al igual que en el caso del seguro
                    de accidentes personales, el beneficio de asistencias personales puede aplicar para el cliente
                    individual o para los beneficiarios que designe el cliente corporativo.

